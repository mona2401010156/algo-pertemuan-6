import math

#Menghitung akar kuadrat dari x.
print(math.sqrt(25))

#Menghitung x^y (x pangkat y).
print (math.pow(3, 2))

#Menghitung nilai absolut (nilai mutlak) dari x.
print (math.fabs(-19))

#Menghitung faktorial dari x.
print(math.factorial(3))

#Mengembalikan nilai pembulatan ke atas (ceiling)
print(math.ceil(2.4))

#Mengembalikan nilai pembulatan ke bawah (floor)
print(math.floor(7.4))
